// Упражнение 15.2. Чем спецификатор доступа protected отличается от private?

// Спецификатор доступа protected позволяет иметь доуступ к членам базового
// класса наследуемым классам. Для пользователей классов нет никакой разницы.
// Как private, но только строго для пользователей, а не для производных
// классов.
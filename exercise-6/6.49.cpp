/* Функция кандидат - это функция, которая состоит в наборе, в котом будут
 * находить максиально подходящую функцию для вызова.
 *
 * Подходящая функция - это функция или одна из функций, которая подходит по
 * количеству аргуметнов и их типам, которые полностью соответствуют типам
 * параметров или могут быть преобразованы.*/
// Упражнение 5.2. Что такое блок? Когда его можно использовать?

/* Блок или же составлной оператор - поседовательносить операторов, заключенные
 * в круглые скобки. Есть собственная область видимости. Ставится там, где
 * синтакис требует один оператор, а логика программы несколько.
 *
 * Пример с циклом.
 *
 * while (val <= 10) {
 *  sum += val;
 *  ++val;
 * }
 * В тело цикла требуется один оператор, но блок или же составной оператор
 * позволяет писать большее количество операторов.
 * */